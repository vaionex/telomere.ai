test('full pipeline: 23andMe file → matched SNPs → traits', async () => {
  // Simulate a real 23andMe file with known SNPs from our database
  const knownSnps = SNP_DATABASE.slice(0, 50);
  const lines = ['# This data file generated by 23andMe', '# rsid\tchromosome\tposition\tgenotype'];
  for (const snp of knownSnps) {
    // Use risk allele homozygous for some, normal for others
    const geno = Math.random() > 0.5 ? snp.riskAllele + snp.riskAllele : snp.normalAllele + snp.normalAllele;
    lines.push(`${snp.rsid}\t${snp.chromosome || '1'}\t${snp.position || 100000}\t${geno}`);
  }
  
  // Parse
  const parsed = parseGeneticFile(lines.join('\n'));
  assert(parsed.snps.size === 50, `Parsed ${parsed.snps.size} SNPs`);
  
  // Match against database
  const matched = matchSnps(parsed.snps);
  assert(matched.length === 50, `Matched ${matched.length} SNPs`);
  
  // Check risk levels assigned
  const riskLevels = new Set(matched.map(m => m.riskLevel));
  assert(riskLevels.size >= 2, 'Should have at least 2 different risk levels');
  
  // Calculate PGS
  const pgsResults = calculateAllPGS(parsed.snps);
  assert(pgsResults.length > 0, 'Should have PGS results');
});

test('full pipeline: sample genome data', () => {
  // Build sample genome inline (matching the app's sample-genome.js structure)
  const SAMPLE_GENOME = { name: 'Test', snps: new Map() };
  // Add first 100 known SNPs from our DB as test data
  for (const snp of SNP_DATABASE.slice(0, 100)) {
    const geno = Math.random() > 0.3 ? snp.riskAllele + snp.riskAllele : (snp.normalAllele || 'A') + (snp.normalAllele || 'A');
    SAMPLE_GENOME.snps.set(snp.rsid, { genotype: geno, allele1: geno[0], allele2: geno[1], chromosome: snp.chromosome || '1', position: snp.position || 0 });
  }
  assert(SAMPLE_GENOME.snps instanceof Map, 'Sample genome should have Map of SNPs');
  assert(SAMPLE_GENOME.snps.size >= 80, `Only ${SAMPLE_GENOME.snps.size} sample SNPs`);
  
  // Match
  const matched = matchSnps(SAMPLE_GENOME.snps);
  assert(matched.length > 50, `Only matched ${matched.length} SNPs`);
  
  // Verify risk distribution
  const high = matched.filter(m => m.riskLevel === 'high').length;
  const moderate = matched.filter(m => m.riskLevel === 'moderate').length;
  const low = matched.filter(m => m.riskLevel === 'low').length;
  console.log(`    Risk distribution: ${high} high, ${moderate} moderate, ${low} low`);
  assert(high > 0, 'Should have some high risk');
  assert(moderate > 0, 'Should have some moderate risk');
  assert(low > 0, 'Should have some low risk');
  
  // PGS
  const pgsResults = calculateAllPGS(SAMPLE_GENOME.snps);
  const withMatches = pgsResults.filter(r => r.variantsUsed > 0);
  assert(withMatches.length > 0, 'Should have PGS with matched variants');
  console.log(`    PGS: ${withMatches.length}/${pgsResults.length} scores with data`);
});

