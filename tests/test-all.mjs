test('VCF format parsed correctly', () => {
  const data = [
    '##fileformat=VCFv4.1',
    '##source=test',
    '#CHROM\tPOS\tID\tREF\tALT\tQUAL\tFILTER\tINFO\tFORMAT\tSAMPLE',
    '1\t82154\trs4477212\tG\tA\t.\tPASS\t.\tGT\t1/1',
    '1\t752566\trs3094315\tA\tG\t.\tPASS\t.\tGT\t0/1',
    '1\t752721\trs3131972\tA\tG\t.\tPASS\t.\tGT\t1/1',
  ].join('\n');
  const r = parseGeneticFile(data);
  assert(r.format === 'vcf', `Format was ${r.format}`);
  assert(r.snps.size === 3);
  const snp = r.snps.get('rs4477212');
  assert(snp.genotype === 'AA', `Genotype was ${snp.genotype}`);
});

test('unknown format throws', () => {
  try {
    parseGeneticFile('just some random text\nwith no structure');
    assert(false, 'Should have thrown');
  } catch (e) {
    assert(e.message.includes('Unable to detect'), `Wrong error: ${e.message}`);
  }
});

